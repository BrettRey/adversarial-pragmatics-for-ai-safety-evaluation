    def test_restricted_symlink_is_rejected_before_target_chmod(self) -> None:
        with tempfile.TemporaryDirectory(
            prefix="naturalistic-pragmatic-extremes-vault-",
            dir=ROOT / "private" / "restricted",
        ) as vault_temp, tempfile.TemporaryDirectory(
            dir=ROOT / "private"
        ) as outside_temp:
            vault = Path(vault_temp)
            target = Path(outside_temp) / "target.txt"
            target.write_text("preserve", encoding="utf-8")
            target.chmod(0o644)
            (vault / "id-key.bin").symlink_to(target)

            with self.assertRaisesRegex(SystemExit, "contains a symlink"):
                self.module.assert_restricted_tree_owned(vault)

            self.assertEqual(target.read_text(encoding="utf-8"), "preserve")
            self.assertEqual(target.stat().st_mode & 0o777, 0o644)

    def test_restricted_unknown_entry_is_rejected_and_preserved(self) -> None:
        with tempfile.TemporaryDirectory(
            prefix="naturalistic-pragmatic-extremes-vault-",
            dir=ROOT / "private" / "restricted",
        ) as vault_temp:
            vault = Path(vault_temp)
            unknown = vault / "unexpected-private-record.txt"
            unknown.write_text("preserve", encoding="utf-8")

            with self.assertRaisesRegex(SystemExit, "unexpected entry"):
                self.module.assert_restricted_tree_owned(vault)

            self.assertEqual(unknown.read_text(encoding="utf-8"), "preserve")

