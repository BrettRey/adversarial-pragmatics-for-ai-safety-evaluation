    def test_synthetic_end_to_end(self) -> None:
        with tempfile.TemporaryDirectory(
            prefix="naturalistic-pragmatic-extremes-test-",
            dir=ROOT / "private" / "discovery",
        ) as corpus_temp, tempfile.TemporaryDirectory(
            prefix="naturalistic-pragmatic-extremes-test-",
            dir=ROOT / "private" / "restricted",
        ) as restricted_temp:
            out_dir = Path(corpus_temp)
            restricted_dir = Path(restricted_temp)
            completed = subprocess.run(
                [
                    sys.executable,
                    str(SCRIPT),
                    "--codex-root",
                    str(FIXTURES / "codex"),
                    "--claude-root",
                    str(FIXTURES / "claude"),
                    "--out-dir",
                    str(out_dir),
                    "--restricted-dir",
                    str(restricted_dir),
                    "--review-limit",
                    "20",
                    "--minimum-source-age-minutes",
                    "0",
                ],
                cwd=ROOT,
                text=True,
                capture_output=True,
                check=False,
            )
            self.assertEqual(completed.returncode, 0, completed.stderr)
            candidates = read_jsonl(out_dir / "candidate-index.jsonl")
            classes = {(row["source"], row["candidate_class"]) for row in candidates}
            self.assertEqual(
                classes,
                {
                    ("codex", "likely_pragmatic_failure"),
                    ("codex", "surprising_success_candidate"),
                    ("claude", "likely_pragmatic_failure"),
                    ("claude", "surprising_success_candidate"),
                },
            )
            self.assertEqual(len(candidates), 4)
            self.assertTrue(all(re.fullmatch(r"NPE2-[A-F0-9]{24}", row["candidate_id"]) for row in candidates))
            self.assertTrue(all(re.fullmatch(r"SES2-[A-F0-9]{20}", row["session_ref"]) for row in candidates))
            forbidden_fields = {
                "raw_path",
                "session_id",
                "triggering_request",
                "model_visible_response",
                "tool_trace",
                "user_followup",
            }
            self.assertFalse(any(forbidden_fields & set(row) for row in candidates))
            self.assertFalse(
                any(
                    forbidden in json.dumps(candidates).lower()
                    for forbidden in (
                        "hidden reasoning",
                        "synthetic api error",
                        "compaction summary must not",
                        "task notification must not",
                        "alternate branch must not",
                    )
                )
            )
            review_rows = read_jsonl(out_dir / "review-corpus.jsonl")
            serialized_review = json.dumps(review_rows)
            self.assertEqual(len(review_rows), 4)
            self.assertNotIn("/Users/", serialized_review)
            self.assertNotIn("http://", serialized_review)
            self.assertNotIn("https://", serialized_review)
            self.assertNotIn("@example", serialized_review)
            self.assertFalse(any("text" in action for row in review_rows for action in row["tool_action_classes"]))
            direct_fields = {
                "triggering_request",
                "model_visible_response",
                "user_followup",
                "immediate_model_response",
            }
            for row in review_rows:
                withheld = set(row["content_withheld_fields"])
                for field in direct_fields & withheld:
                    self.assertEqual(row[field], "")
                if "preceding_context" in withheld:
                    self.assertEqual(row["preceding_context"], [])
            audit = json.loads(
                (out_dir / "reports" / "retrieval-audit.json").read_text(encoding="utf-8")
            )
            self.assertEqual(audit["sessions_parsed"], 2)
            self.assertEqual(audit["exclusions"]["codex"]["subagent"], 1)
            self.assertEqual(audit["exclusions"]["claude"]["subagent"], 1)
            self.assertEqual(audit["exclusions"]["claude"]["excluded_session_mode"], 1)
            self.assertEqual(audit["parse_errors"], [])
            self.assertFalse(audit["raw_episode_derivative_retained"])
            self.assertTrue((restricted_dir / "id-key.bin").is_file())
            self.assertTrue((restricted_dir / "source-linkage.csv").is_file())
            self.assertTrue((restricted_dir / "source-fingerprints.csv").is_file())
            self.assertFalse((out_dir / "source-index.jsonl").exists())
            self.assertFalse((out_dir / "all-candidates.jsonl").exists())
            manifest = json.loads((out_dir / "corpus-manifest.json").read_text())
            self.assertEqual(manifest["schema_version"], 2)
            residual = json.loads((out_dir / "reports" / "residual-risk-audit.json").read_text())
            self.assertTrue(
                all(value["matches"] == 0 for value in residual["residual_identifier_counts"].values())
            )

            review_dir = out_dir / "review"
            review = subprocess.run(
                [
                    sys.executable,
                    str(REVIEW_SCRIPT),
                    "--candidates",
                    str(out_dir / "review-corpus.jsonl"),
                    "--out-dir",
                    str(review_dir),
                ],
                cwd=ROOT,
                text=True,
                capture_output=True,
                check=False,
            )
            self.assertEqual(review.returncode, 0, review.stderr)
            html = (review_dir / "index.html").read_text(encoding="utf-8")
            self.assertIn("Private pragmatic-extremes review", html)
            self.assertNotIn(candidates[0]["candidate_id"], html)
            self.assertNotIn("localStorage", html)


