#!/usr/bin/env python3
"""Focused tests for the private naturalistic pragmatic-extremes workflow."""

from __future__ import annotations

import hashlib
import importlib.util
import json
import subprocess
import sys
import tempfile
import unittest
import re
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
SCRIPT = ROOT / "scripts" / "build_naturalistic_pragmatic_corpus.py"
REVIEW_SCRIPT = ROOT / "scripts" / "build_pragmatic_extremes_review.py"
FIXTURES = ROOT / "data" / "fixtures" / "naturalistic-corpus"


def load_module():
    spec = importlib.util.spec_from_file_location("naturalistic_corpus", SCRIPT)
    if spec is None or spec.loader is None:
        raise RuntimeError("could not load corpus builder")
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


def read_jsonl(path: Path) -> list[dict]:
    with path.open(encoding="utf-8") as handle:
        return [json.loads(line) for line in handle if line.strip()]


class NaturalisticCorpusTest(unittest.TestCase):
    @classmethod
    def setUpClass(cls) -> None:
        cls.module = load_module()

    def test_plain_negative_constraint_is_not_a_repair(self) -> None:
        signals = self.module.failure_signals("Do not edit this file.")
        self.assertLess(self.module.signal_score(signals), 3)

    def test_minimizer_redacts_and_withholds_hostile_content(self) -> None:
        sanitizer = self.module.EpisodeSanitizer(["fixtureowner"])
        safe = sanitizer.sanitize(
            "Ask Fixture Person to inspect /Users/fixtureowner/private/example.tex "
            "and reply to person@example.test on 2026-07-15.",
            1600,
        )
        self.assertNotIn("fixtureowner", safe.text.lower())
        self.assertNotIn("person@example", safe.text.lower())
        self.assertNotIn("/Users/", safe.text)
        self.assertNotIn("2026-07-15", safe.text)
        hostile = sanitizer.sanitize("</script><script>fetch('https://example.test')</script>", 1600)
        self.assertEqual(hostile.withheld_reason, "active_content")
        self.assertNotIn("<script", hostile.text.lower())

    def test_source_reader_rejects_malformed_json_and_invalid_utf8(self) -> None:
        with tempfile.TemporaryDirectory(dir=ROOT / "private") as temporary:
            root = Path(temporary)
            valid = root / "valid.jsonl"
            valid.write_bytes(b'{"valid": true}\n\n{"also": "valid"}\n')
            hasher = hashlib.sha256()
            self.assertEqual(len(list(self.module.read_json_lines(valid, hasher))), 2)
            self.assertEqual(hasher.hexdigest(), hashlib.sha256(valid.read_bytes()).hexdigest())

            malformed = root / "malformed.jsonl"
            malformed.write_text('{"valid": true}\nnot-json\n', encoding="utf-8")
            with self.assertRaisesRegex(ValueError, "invalid JSON object"):
                list(self.module.read_json_lines(malformed))

            invalid_utf8 = root / "invalid-utf8.jsonl"
            invalid_utf8.write_bytes(b'{"valid": true}\n\xff\n')
            with self.assertRaises(UnicodeDecodeError):
                list(self.module.read_json_lines(invalid_utf8))

    def test_discovery_digest_rejects_between_read_mutation(self) -> None:
        with tempfile.TemporaryDirectory(dir=ROOT / "private") as temporary:
            root = Path(temporary)
            source = root / "session.jsonl"
            source.write_bytes((FIXTURES / "codex" / "root-session.jsonl").read_bytes())
            discovered, excluded = self.module.discover_paths(
                root,
                "codex",
                self.module.date(2026, 1, 1),
                self.module.date(2026, 12, 31),
                allowed_modes={"cli"},
            )
            self.assertEqual(excluded["malformed_source"], 0)
            self.assertEqual(len(discovered), 1)
            path, meta = discovered[0]

            mutated = path.read_bytes().replace(
                b"synthetic-codex-root", b"synthetic-codex-xxxx", 1
            )
            self.assertNotEqual(mutated, path.read_bytes())
            path.write_bytes(mutated)
            parsed_hasher = hashlib.sha256()
            self.module.parse_codex(path, meta, 12_000, parsed_hasher)

            self.assertFalse(
                self.module.discovery_snapshot_matches(
                    meta, parsed_hasher.hexdigest(), self.module.source_state(path)
                )
            )

